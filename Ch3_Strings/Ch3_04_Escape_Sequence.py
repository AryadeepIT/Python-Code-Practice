story="Aryadeep is a good boy \n He is very good" # \n --> for new line
print(story)

story1="Aryadeep is a good boy \n  He \t is very good" #\t --> tab print
print(story1)

story2="Aryadeep is a good boy \\ He is very good" #\\ --> single backslash print
print(story2)

story3="Aryadeep is a good boy \' He is very good" #\' --> single quote
print(story3)