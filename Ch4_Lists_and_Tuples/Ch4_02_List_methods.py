l1= [1,67,89,25]
l1.sort()
print(l1) #for sorting in ascending order
l1.reverse()
print(l1) #for sorting decending order

l1.append("Arya") #adds Arya in the end of the list
print(l1)

l1.insert(0, 542) #replace the value of 0th index to 542
print(l1)

l1.pop(2) #remove the 2nd index value #67 removed
print(l1)

l1.remove("Arya") #remove "Arya" from list
print(l1)